def generate_sermon(*, user_name: str | None, mood: str, scripture_ref: str, target_minutes: int = 8, language: str = "en", prayer_text: str | None = None, user_history: dict | None = None) -> str:
    """Preaching segment, built around the user's mood and the chosen passage."""
    lang_inst = _language_instruction(language) if language != "en" else ""
    system = (
        (f"{lang_inst} " if lang_inst else "") +
        "You are an expository preacher writing for spoken delivery by a digital avatar. "
        "Move through three movements — a hook that draws the listener in, the passage's "
        "context and meaning, then how it applies to their life today — but write it as "
        f"one continuous spoken message. Target {target_minutes} minutes (~130-150 "
        "words/min). Plain spoken prose ONLY: no headings, no section labels, no bullet "
        "points, no markdown (no #, *, _, ---), and no stage directions or bracketed cues "
        "of any kind. If the theme touches a serious medical or safety emergency, do not "
        "advise; close gently and defer to real-world help."
    )
    freshness = _freshness_directive(user_history)
    if freshness:
        system = f"{system} {freshness}"
    user = (
        f"Scripture focus: {scripture_ref}\n"
        f"Listener's theme/mood: {mood}\n"
        "Do NOT address the listener by name and do NOT invent one — never write any "
        "personal name for them anywhere in the message. Speak to them warmly using "
        "only 'you'."
    )
    if anchor := _keyword_anchor(prayer_text):
        user = f"{user}\n{anchor}"
    # Local Myanmar/Tedim Ollama models on small CPU boxes are much slower than the
    # hosted English chat model. Keep those messages shorter so the text pages land
    # reliably and narration is not asked to synthesize an eight-minute segment.
    if language in ("my", "td"):
        max_tokens = 600 if target_minutes <= 5 else 900
    else:
        max_tokens = 1500 if target_minutes <= 5 else 2500
    try:
        text = _strip_formatting(_complete(system, user, max_tokens=max_tokens, language=language))
        if language == "my" and not _is_my_plausible(text, min_myanmar_chars=80):
            print(f"[llm] Burmese sermon too short/garbled ({len(text)} chars), using corpus fallback", flush=True)
            return _fallback_sermon(mood, scripture_ref, language)
    except Exception as exc:
        print(f"[llm] {language} sermon fallback: {exc}", flush=True)
        return _fallback_sermon(mood, scripture_ref, language)
    return _strip_name(text, user_name)


