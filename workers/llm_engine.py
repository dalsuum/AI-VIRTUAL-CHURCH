"""
LLM engine — turns the logged-in user's intake (name, mood, prayer text) into the
spoken content of the service: opening prayer, scripture selection, sermon, and
benediction. The user's own words drive every segment, so no two services match.

Scripture text is NOT generated by the model (copyright + accuracy). The model only
chooses a reference; full verse text is resolved separately via a licensed Bible API.
"""

from __future__ import annotations

import json
import os
import re

import requests


_TEDIM_CORRECTIONS = [
    # Mizo word       → Tedim word      (word-boundary, case-insensitive)
    (r"\bkohhran\b",   "koici"),        # church
    (r"\btawngtaina\b","thungetna"),    # prayer (noun)
    (r"\btawngtai\b",  "thungeta"),     # pray (verb)
    (r"\bpathian\b",   "Pasian"),       # God
    (r"\blalpa\b",     "Topa"),         # the Lord
    (r"\bisua\b",      "Zeisu"),        # Jesus
]
_TEDIM_CORRECTIONS_COMPILED = [
    (re.compile(pat, re.IGNORECASE), rep) for pat, rep in _TEDIM_CORRECTIONS
]


def _fix_tedim_vocab(text: str) -> str:
    for pattern, replacement in _TEDIM_CORRECTIONS_COMPILED:
        text = pattern.sub(replacement, text)
    return text


def _strip_formatting(text: str) -> str:
    """Reduce model output to clean spoken prose.

    Free models tend to dress a sermon up as a *script* — markdown headings, bold,
    bullet lists, horizontal rules, and bracketed stage directions like
    ``**[Soft instrumental intro]**`` or ``[Pause - 3 seconds]``. None of that should
    be shown to (or read aloud to) the worshipper, so we strip it here, leaving the
    words. Applied to every spoken segment, so the prompt and the model can't leak
    formatting into the service."""
    # Stage directions / cues in brackets, with any wrapping emphasis: **[...]**, [pause]
    text = re.sub(r"\*{0,3}\[[^\]]*\]\*{0,3}", "", text)
    # Headings (leading #), and horizontal-rule lines (---, ***, ___)
    text = re.sub(r"(?m)^\s{0,3}#{1,6}\s*", "", text)
    text = re.sub(r"(?m)^\s*([-*_])\1{2,}\s*$", "", text)
    # List markers at the start of a line (-, *, +, "1.")
    text = re.sub(r"(?m)^\s*(?:[-*+]|\d+\.)\s+", "", text)
    # Bold/italic emphasis — keep the words, drop the markers
    text = re.sub(r"\*{1,3}([^*\n]+?)\*{1,3}", r"\1", text)
    text = re.sub(r"_{1,3}([^_\n]+?)_{1,3}", r"\1", text)
    text = text.replace("**", "").replace("__", "")
    # Tidy whitespace: no leading/trailing spaces per line, at most one blank line
    # between paragraphs (the player renders with white-space: pre-wrap)
    text = re.sub(r"(?m)^[ \t]+", "", text)
    text = re.sub(r"[ \t]+\n", "\n", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def _strip_name(text: str, user_name: str | None) -> str:
    """Safety net: scrub the worshipper's name out of generated prose.

    The prompt tells the model not to address the listener by name, but free models
    slip and drop it in anyway (``"Test Admin, imagine..."``). For segments that must
    never expose the name (the sermon/message), we also remove any literal occurrence
    of it — the full name and each of its words — and repair the punctuation/casing
    left behind by a removed vocative."""
    name = (user_name or "").strip()
    if not name:
        return text
    # Full name first, then each word (longest-first so "Test Admin" beats "Test").
    parts = [p for p in re.split(r"\s+", name) if len(p) >= 2]
    for cand in sorted({name, *parts}, key=len, reverse=True):
        c = re.escape(cand)
        # Opening vocative — "Name, imagine ..." -> "Imagine ..." (recapitalize).
        text = re.sub(
            rf"(?m)(?:^|(?<=[.!?]\s)){c}\s*,\s*(\w)",
            lambda m: m.group(1).upper(),
            text,
        )
        # Trailing/inline vocative — ", Name." / ", Name," -> drop the name.
        text = re.sub(rf",\s*{c}\b", "", text)
        # Any remaining standalone mention.
        text = re.sub(rf"\b{c}\b", "", text)
    # Tidy spacing and orphaned punctuation left by the removals.
    text = re.sub(r"\s+([,.!?;:])", r"\1", text)
    text = re.sub(r"[ \t]{2,}", " ", text)
    return text.strip()


def _ensure_exact_name(text: str, user_name: str | None) -> str:
    """No-op: names are never injected into service output."""
    return text


# We talk to OpenRouter's OpenAI-compatible Chat Completions endpoint. Any provider
# that speaks that format (OpenRouter, OpenAI, local vLLM, …) works by changing the
# base URL + key + model, with no code change.
API_KEY = os.environ["OPENROUTER_API_KEY"]
BASE_URL = os.getenv("OPENROUTER_BASE_URL", "https://openrouter.ai/api/v1")
MODEL = os.getenv("LLM_MODEL", "meta-llama/llama-3.3-70b-instruct:free")
LOCAL_LLM_TIMEOUT = int(os.getenv("LOCAL_LLM_TIMEOUT", "300"))


def _model_for(language: str = "en") -> str:
    """Allow stronger models for low-resource service languages.

    Some free English-first models emit Burmese-looking text that is not natural
    Myanmar. Operators can set LLM_MODEL_MY or LLM_MODEL_TD without changing the
    English service model. Placeholder/example values are ignored so a copied
    .env cannot break generation with an invalid OpenRouter model id.
    """
    override = (os.getenv(f"LLM_MODEL_{language.upper()}") or "").strip()
    if override and not override.startswith(("your-", "change-me", "example")):
        return override
    return MODEL


def _local_llm_url(language: str) -> tuple[str, str] | None:
    if language == "my":
        base = (os.getenv("BURMESE_LLM_URL") or "").rstrip("/")
        return (base, "/burmese/generate") if base else None
    if language == "td":
        base = (os.getenv("TEDIM_LLM_URL") or "").rstrip("/")
        return (base, "/tedim/generate") if base else None
    return None


def _should_use_local_llm(system: str, language: str) -> bool:
    # The intake plan must stay on the chat model because it is a strict JSON
    # contract. Local low-resource generators are used for prose segments only.
    return language in ("my", "td") and "Output ONLY valid JSON" not in system


def _complete_local(system: str, user: str, max_tokens: int, language: str) -> str:
    endpoint = _local_llm_url(language)
    if not endpoint:
        raise RuntimeError(f"No local LLM URL configured for language {language!r}")
    base, path = endpoint
    resp = requests.post(
        f"{base}{path}",
        json={"system": system, "prompt": user, "max_tokens": max_tokens},
        timeout=LOCAL_LLM_TIMEOUT,
    )
    resp.raise_for_status()
    return (resp.json().get("text") or "").strip()


def _complete(system: str, user: str, max_tokens: int = 1500, language: str = "en") -> str:
    if _should_use_local_llm(system, language):
        text = _complete_local(system, user, max_tokens, language)
        if language == "td":
            text = _fix_tedim_vocab(text)
        return text

    resp = requests.post(
        f"{BASE_URL}/chat/completions",
        headers={
            "Authorization": f"Bearer {API_KEY}",
            "Content-Type": "application/json",
        },
        json={
            "model": _model_for(language),
            "max_tokens": max_tokens,
            "messages": [
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ],
        },
        timeout=120,
    )
    resp.raise_for_status()
    text = (resp.json()["choices"][0]["message"]["content"] or "").strip()
    if language == "td":
        text = _fix_tedim_vocab(text)
    return text


def _addressing(user_name: str | None) -> str:
    """Always address the worshipper anonymously — names are never used in output."""
    return (
        "Do NOT address the worshipper by name and do NOT invent one. "
        "Speak to them warmly without using any name."
    )


def _freshness_directive(user_history: dict | None) -> str:
    """Build a system-prompt clause that prevents repeating past service content.

    The worker passes the user's last ~10 sessions (same language) so the LLM
    can pick a different scripture passage, avoid recycled illustrations, and
    find fresh angles — ensuring every return visit feels genuinely new."""
    if not user_history:
        return ""
    parts = []
    past_refs = [r for r in (user_history.get("past_scripture_refs") or []) if r][:8]
    if past_refs:
        parts.append(
            "IMPORTANT — these Bible passages were already used in this person's past services; "
            f"choose a DIFFERENT passage: {', '.join(past_refs)}."
        )
    past_excerpts = [e for e in (user_history.get("past_prayer_excerpts") or []) if e][:4]
    if past_excerpts:
        parts.append(
            "Past prayer themes already addressed for this person: "
            + "; ".join(f'"{e}"' for e in past_excerpts)
            + ". Bring a genuinely fresh angle — a new illustration, metaphor, or facet "
            "of grace not yet explored."
        )
    return " ".join(parts)


def _keyword_anchor(prayer_text: str | None) -> str:
    """Surface the user's own feeling-words so the LLM grounds content in them.

    The prayer_text already flows into the user-turn prompt, but a short,
    clearly-labelled anchor phrase nudges the model to treat those exact words
    as the emotional centre of the segment rather than just context."""
    text = (prayer_text or "").strip()
    if not text:
        return ""
    snippet = text[:150].rsplit(" ", 1)[0] if len(text) > 150 else text
    return (
        f'The worshipper\'s own words: "{snippet}". '
        "Ground this segment directly in these specific feelings and concerns."
    )



