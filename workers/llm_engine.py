def _language_instruction(language: str) -> str:
    """One system-prompt line fixing the output language of a spoken segment.

    'my' services are conducted entirely in Burmese. The instruction pins the
    script too — Myanmar Unicode only, never Zawgyi or romanization — because the
    player renders with Unicode fonts (Padauk/Noto Sans Myanmar) and edge-tts's
    my-MM voices expect Unicode text.

    'td' services are conducted in Tedim Chin (Zolai / Zomi pau, Latin script).
    The instruction anchors the model with the community's own Christian
    register — Pasian (God), Topa (the Lord), Zeisu (Jesus) — the vocabulary of
    the Lai Siangtho (1932) and the ZBC hymnal, which the scripture and hymn
    segments already use."""
    if language == "my":
        return (
            "Write your ENTIRE response in the Burmese (Myanmar) language, using "
            "Myanmar Unicode script only (never Zawgyi, never romanized Burmese, "
            "no English words, except a worshipper's proper name when one is provided). "
            "Compose directly in natural Myanmar Burmese; do not translate English "
            "idioms word-for-word. Use simple, meaningful sentences and a reverent "
            "Christian worship register that ordinary Myanmar readers can understand. "
            "For narration, spell out numbers and Bible references in Burmese words; "
            "do not leave raw forms like 'Jn 3:16' or '2026'."
        )
    if language == "td":
        return (
            "LANGUAGE LAW — ABSOLUTE: Write EVERY SINGLE SENTENCE in Tedim Chin (Zolai / Zomi pau) ONLY. "
            "ZERO English sentences, ZERO English paragraphs, ZERO English explanations. "
            "Do NOT translate, do NOT add English alongside Tedim, do NOT explain in English. "
            "Every word must be Tedim. The only Latin letters allowed are Tedim words. "
            "Use standard Tedim Latin orthography only — no Burmese script. "
            "CRITICAL: Tedim is NOT Mizo, NOT Falam Chin, and NOT Haka Chin. Do NOT use Mizo words. "
            "These Tedim words are REQUIRED — use them exactly: "
            "Pasian (God) — NOT Pathian; "
            "Topa (the Lord) — NOT Lal or Lalpa; "
            "Zeisu Krist (Jesus Christ) — NOT Isua; "
            "Kha Siangtho (Holy Spirit); "
            "thungetna (prayer) — NOT tawngtaina; "
            "koici (church) — NOT kohhran; "
            "zangtal (salvation); "
            "lungdamna (grace/blessing); "
            "Thu Siangtho (Holy Scripture); "
            "Siangtho (holy/sacred); "
            "Zomi (the Tedim people). "
            "Tedim declarative sentences end with 'hi'; benedictive endings use 'hen'. "
            "Common Tedim sentence patterns: 'Pasian in i hiam hi' (God loves you), "
            "'Topa in na ngaih hi' (the Lord hears you), 'Zeisu in zangtal piak hi' (Jesus gives salvation). "
            "Keep sentences short and clear. Compose directly in Tedim; never translate English idioms. "
            "For narration, spell out numbers and Bible references in Tedim words; do not leave raw forms like 'Jn 3:16' or '2026'."
        )
    return ""


